/** Normalize word for lookup: lowercase, trim, remove trailing punctuation. */
function normalizeKey(word: string): string {
  return word.replace(/^[,.\-—;:'"!?()]+|[,:.\-—;:'"!?()]+$/g, '').trim().toLowerCase() || word.toLowerCase()
}

/** Word-level gloss map: normalized spanishWord -> rich token data (no spanishWord).
 * Built from TOKEN_MAP so any word gets full tooltip regardless of which line it appears in. */
const WORD_GLOSS_MAP: Record<string, Omit<WordToken, 'spanishWord'>> = (() => {
  const map: Record<string, Omit<WordToken, 'spanishWord'>> = {}
  for (const tokens of Object.values(TOKEN_MAP)) {
    for (const t of tokens) {
      const key = normalizeKey(t.spanishWord)
      if (!map[key]) map[key] = { englishGloss: t.englishGloss, lemma: t.lemma, grammarNote: t.grammarNote, contextInSong: t.contextInSong }
      // Also map raw form for exact matches (e.g. "movie'" vs "movie")
      const rawKey = t.spanishWord.toLowerCase().trim()
      if (!map[rawKey]) map[rawKey] = { englishGloss: t.englishGloss, lemma: t.lemma, grammarNote: t.grammarNote, contextInSong: t.contextInSong }
    }
  }
  return map
})()

/** Generate tokens with word-level lookup: use rich data from WORD_GLOSS_MAP when available */
function generateEnrichedTokens(spanishText: string): WordToken[] {
  const words = spanishText.split(/\s+/).filter(Boolean)
  return words.map((word) => {
    const cleaned = word.replace(/^[,.\-—;:'"!?()]+|[,:.\-—;:'"!?()]+$/g, '').trim() || word
    const key = normalizeKey(cleaned)
    const gloss = WORD_GLOSS_MAP[key] ?? WORD_GLOSS_MAP[cleaned.toLowerCase()]
    if (gloss) {
      return { spanishWord: cleaned, ...gloss }
    }
    return { spanishWord: cleaned, englishGloss: cleaned, lemma: undefined, grammarNote: undefined }
  })
}

/** Get tokens for a line - from TOKEN_MAP if available, else word-level enriched fallback */
export function getTokensForLine(spanishText: string): WordToken[] {
  const exact = TOKEN_MAP[spanishText]
  if (exact) return exact
  return generateEnrichedTokens(spanishText)
}

